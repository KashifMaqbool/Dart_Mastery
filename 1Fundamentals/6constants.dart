void main(){
    Final();
    Const();
}
Final(){
    print("Final keyword:");
    final a = 10;
    final int b = 50;
    print(a);
    print(b);
}

Const(){
    print("");
    print("Constant keyword:");
    const a = 10;
    const int b = 50;
    print(a);
    print(b);
}
    